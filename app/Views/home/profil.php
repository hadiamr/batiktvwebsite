                        &emsp;Lahirnya UU Penyiaran tahun 2002 membawa angin segar bagi demokratisasi penyiaran. Televisi tidak lagi terpusat di Jakarta, stasiun-stasiun televisi lokal dapat berdiri, baik sebagai stasiun independen maupun menjadi bagian dari jaringan stasiun televisi nasional. Inilah gagasan dasar bahwa Kota Kecil Pekalongan perlu memiliki stasiun televisi. Gagasan ini kali pertama muncul pada saat Pemerintah Kota Pekalongan dipimpin oleh Wali Kota Samsudiat pada tahun 2002. Batik TV Pekalongan lahir melalui proses panjang dan membutuhkan energi serta perjuangan yang tidak mudah. <br><br>

                        &emsp;Di tahun 2011 Wali Kota Pekalongan H.M Basyir Ahmad Wali Kota Pekalongan, bertekad mewujudkan bahwa Kota Pekalongan harus memiliki Televisi sendiri. Diawali dengan kerjasama Pemkot Pekalongan dengan Litbang Institut Kesenian Jakarta & TV Komunitas Grabah Magelang pada tahun 2011. Dilanjutkan dengan penandatanganan MoU antara Pemkot Pekalongan Dekan Fakultas Film dan TV IKJ pada tahun 2011 serta pendampingan yang intens. Semuanya itu kemudian diperkuat dengan disetujuinya APBD Pemerintah Kota Pekalongan oleh DPRD Kota Pekalongan pada tahun anggaran 2012 serta Perda No.1 Tahun 2012 Pemerintah Kota Pekalongan. <br><br>

                        &emsp;Hingga akhirnya LPPL Batik TV resmi mengantongi ijin Surat Keputusan Menteri Komunikasi dan Informatika RI No. 682 tentang Izin Prinsip Penyelenggaraan Penyiaran (IP3) LPPL Batik TV Kota Pekalongan pada tanggal 12 November 2012 dan Surat Keputusan Menteri Komunikasi dan Informatika RI No. 973 Tahun 2014 tentang Izin Penyelenggaraan Penyiaran (IP2) LPPL Jasa Penyiaran Televisi “Batik TV Pekalongan” pada tanggal 12 November 2014 dengan alokasi frekuensi pada kanal 57 UHF. <br><br>

                        &emsp;Diawal tahun berdirinya, Batik TV launching dan ujicoba siaran pada 1 April tahun 2012, dan lahir sejak disetujuinya Perda No.1 Tahun 2012 tanggal 16 Mei 2012. Batik TV mempunyai Tag Line "AJIB", ungkapan khas dialek Kota Pekalongan yang berarti ungkapan atas ketakjuban yang bersifat positif, bisa juga berarti bagus, super, baik dan itu biasa diungkapkan oleh masyarakat Kota Pekalongan karena memang khasanah konten lokal dan kearifan lokal menjadi konten utama kami. Menyajikan berbagai informasi yang dibutuhkan masyarakat berbasis kedekatan wilayah, kedekatan kebutuhan masyarakat Kota Pekalongan dan sekitarnya. <br><br>

                        &emsp;LPP Lokal Batik TV Pekalongan dijalankan dan dikelola dengan maksud dan tujuan menjadi lembaga penyiaran televisi lokal yang bersifat independen, netral, tidak komersial dan berfungsi melayani kebutuhan masyarakat. Selain itu mendorong partisipasi masyarakat dalam pembangunan, mendorong penguatan good governance, sebagai timbal balik antara pemerintah dan masyarakat, media informasi, pendidikan, hiburan yang sehat bagi masyarakat dan pelestarian daerah sejalan dengan amanat undang-undang No. 32 Tahun 2002 tentang penyiaran, dan kami terus bertekad dan berbenah memberikan layanan informasi kepada publik. <br><br>

                        &emsp;Di tahun 2022, Batik TV memasuki babak baru; menjadi TV Digital. Batik TV menjadi LPPL pertama di Jawa Tengah yang menandatangani MoU dengan TVRI Pusat pada Sabtu (27/11/2021) di Stasiun Pemancar TVRI di Kawasan Gunung Gantungan Kabupaten Tegal terkait pencanangan migrasi Batik TV menjadi TV Digital. Di Januari 2022 Batik TV melakukan ujicoba siaran digital untuk pertama kalinya hingga secara resmi mengadakan Launching Migrasi TV Digital pada 26 Februari 2022.
